'''
Relational operators in Python are used to compare values and return a Boolean result (True or False).
They play a crucial role in conditional statements, loops, and decision-making logic.
'''
a = 10
b = 20

print(a == b)   # False
print(a != b)   # True
print(a > b)    # False
print(a < b)    # True
print(a >= b)   # False
print(a <= b)   # True

